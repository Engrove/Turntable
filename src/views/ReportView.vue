            <ul>
               <li v-for="param in estimatorParams" :key="param.key">
                <strong v-if="reportData.userInput[param.key]">{{ param.label }}:</strong>
                <span v-if="reportData.userInput[param.key]">{{ reportData.userInput[param.key] }}</span>
